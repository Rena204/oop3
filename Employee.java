class employee {
    String name;
    String position;
    String email;
    String phone;
    int wage;
    int age;

    employee(String name, String position, String email, String phone, int wage, int age) {
        this.name = name;
        this.position = position;
        this.email = email;
        this.phone = phone;
        this.wage = wage;
        this.age = age;
    }
}